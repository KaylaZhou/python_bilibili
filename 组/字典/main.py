def dict_1():

    print({1: '你好呀', '1': '我很好', 'A': '北京', 'B': '上海', 'C': '广州', 'D': '深圳'})
# key可以是int, str, (1, '1', 会识别为不同的键)

    print({1: '你好呀', 1: '我很好', 'A': '北京', 'B': '上海', 'C': '广州', 'D': '深圳'})
# 字典不重复

    print({1: '你好呀', '1': '我很好', 'A': {'北京'},
           'B': ['上海'], 'C': '广州', 'D': '深圳'})
# value 值,可以是 str,int,float,list,set,dict


dict_1()
